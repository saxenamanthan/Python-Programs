print("Here is our first program on Arithmetic Calculations")
num1 = input("Please give the value of first number - ")
num2 = input("Please give the value of second number - ")

num1 = float(num1)
num2 = float(num2)

sum = num1 + num2
diff = num1 - num2
multi = num1 * num2
div = num1 / num2
modl= num1 % num2
exp= num1 ** num2
print(f"Sum of first number and second number is {sum}")
print(f"Difference of first number and second number is {diff}")
print(f"Multiplication of first number and second number is {multi}")
print(f"Division of first number and second number is {div}")
print(f"Modulus of first number and second number is {modl}")
print(f"Exponent of first number and second number is {exp}")