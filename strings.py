a =(" Hello World ")
print(f"The length of the string {a} is {len(a)}")
print(f"The upper case of the string {a} is {a.upper()}")
print(f"The lower case of the string {a} is {a.lower()}")
print(f"The title of the string {a} is {a.title()}")
print(f"The capitalize of the string {a} is {a.capitalize()}")
print(f"The replacement of the string {a} is {a.replace("s","b")}")
print(f"The count of the string {a} is {a.count("e")}")
print(f"Is {a} a digit or not {a.isdigit()}")
print(f"Stripping white spaces from both sides {a} is {a.strip()}")
print(f"Stripping white spaces from left side {a} is {a.lstrip()}")
print(f"Stripping white spaces from right side {a} is {a.rstrip()}")
print(f"Stripping white spaces from right side {a} is {a.rstrip()}")

